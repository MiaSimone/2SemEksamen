package FunctionLayer;

public class Shed {

    private int width;
    private int depth;

    public Shed(int width, int depth) {
        this.width = width;
        this.depth = depth;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getDepth() {
        return depth;
    }

    public void setDepth(int depth) {
        this.depth = depth;
    }
}
